def mul(v1, v2): # take user input as int then  multiply them together display the outcome
    return v1 * v2
print(mul(5, 5))

def mod(v1, v2): # take user input as int then together display the outcome
    return v1 % v2
print(mod(12, 5))

def div(v1, v2): # take user input as int then  divide them and display the outcome
    return v1 / v2
print(div(100, 5))